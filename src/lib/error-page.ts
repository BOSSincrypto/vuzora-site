/**
 * Self-contained HTML error page used by the SSR wrapper (`src/server.ts`)
 * and the Start middleware (`src/start.ts`) when the React tree can't
 * render — module-init failures, h3-swallowed throws, or any uncaught
 * server error.
 *
 * MUST stay dependency-free: this page has to render even when the rest
 * of the app fails to import. No React, no Tailwind, no app modules.
 *
 * @module lib/error-page
 */

/**
 * Render a minimal, system-font HTML page with refresh / "Go home" buttons.
 *
 * @returns A complete `<!doctype html>` document as a string, ready to be
 *          returned in a `Response` with `content-type: text/html`.
 */
export function renderErrorPage(): string {
  return `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <title>This page didn't load</title>
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <style>
      body { font: 15px/1.5 system-ui, -apple-system, sans-serif; background: #fafafa; color: #111; display: grid; place-items: center; min-height: 100vh; margin: 0; padding: 1.5rem; }
      .card { max-width: 28rem; width: 100%; text-align: center; padding: 2rem; }
      h1 { font-size: 1.25rem; margin: 0 0 0.5rem; }
      p { color: #4b5563; margin: 0 0 1.5rem; }
      .actions { display: flex; gap: 0.5rem; justify-content: center; flex-wrap: wrap; }
      a, button { padding: 0.5rem 1rem; border-radius: 0.375rem; font: inherit; cursor: pointer; text-decoration: none; border: 1px solid transparent; }
      .primary { background: #111; color: #fff; }
      .secondary { background: #fff; color: #111; border-color: #d1d5db; }
    </style>
  </head>
  <body>
    <div class="card">
      <h1>This page didn't load</h1>
      <p>Something went wrong on our end. You can try refreshing or head back home.</p>
      <div class="actions">
        <button class="primary" onclick="location.reload()">Try again</button>
        <a class="secondary" href="/">Go home</a>
      </div>
    </div>
  </body>
</html>`;
}
